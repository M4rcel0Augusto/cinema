    private void loadTable() {
        
    ComboItemDAO comboItemDAO = new ComboItemDAO();
    List<ComboItem> items = comboItemDAO.ListItems(comboId);
    fillTable(items);

    }
    
    private void fillTable(List<ComboItem> items){
        String columns[]= {"Id", "Nome", "Tipo", "Tamanho","Quantidade", "Pre√ßo", "Custo"};
        String data [][] = new String[items.size()][columns.length];
        
        int i =0;
        
        for(ComboItem ci : items){
            Product p = ci.getProduct();
            double cost = ci.getQuantity() * p.getUnitCost();
            double price = ci.getQuantity() * p.getUnitPrice();
            data[i] = new String[]{
                String.valueOf(p.getId()),
                p.getProductName(),
                String.valueOf(p.getProductType()),
                String.valueOf(p.getSize()),
                String.valueOf(ci.getQuantity()),
                String.valueOf(p.getUnitPrice()),
                
                String.valueOf(price),
                String.valueOf(cost)


                
            };
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(data,columns);
        jTComboZoom.setModel(model);
       
    }

