    private void jBDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDeleteActionPerformed
        //jCBFilterActionPerformed(evt)
        try{
            int selectedRow = jTScreening.getSelectedRow();
            if(selectedRow>=0){
                int id = Integer.parseInt(String.valueOf(jTScreening.getValueAt(selectedRow, 0)));
            
            
            
            int response = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o item "+id+"?");
            //yes=0, no =1, cancel=2
            if(response == JOptionPane.YES_OPTION){//0
                ScreeningDAO screeningDAO = new ScreeningDAO();
                Screening screening = screeningDAO.select(id);
                
                if(screening == null){
                    JOptionPane.showMessageDialog(this, "Produto não encontrado.");
                    return;
                }
                screeningDAO.delete(screening);
                    JOptionPane.showMessageDialog(this, "Produto deletado com sucesso");
                }
                }else{
                JOptionPane.showMessageDialog(this, "Produt não encontrado");
                }
            jCBFilterActionPerformed(evt);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Erro.\nNão foi possível deletar o item");
        
        }
        
    }//GEN-LAST:event_jBDeleteActionPerformed

    private void jBEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditActionPerformed
        try{
            //Preenchendo na parte de edição os dados selecionados
            ScreeningRegistration screeningRegistration = new ScreeningRegistration(this);//Criar um parente para atualizar a lista automaticamente após a edição
            String id =(String)jTScreening.getValueAt(jTScreening.getSelectedRow(), 0);
            
            ScreeningDAO screeningDAO = new ScreeningDAO();
            
            Screening selectedScreening = screeningDAO.select(Integer.parseInt(id));
            
            screeningRegistration.fillUpdate(selectedScreening);
            screeningRegistration.setVisible(true);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Ocorre uma falha.");
            
        }
        
        
    }//GEN-LAST:event_jBEditActionPerformed

