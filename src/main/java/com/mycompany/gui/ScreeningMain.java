   public void loadTable(boolean filter){
   
       ScreeningDAO screeningDAO = new ScreeningDAO();
       List<Screening> screenings = screeningDAO.list(filter);
       fillTable(screenings);
   }
    
    private void fillTable(List<Screening> screenings){
        String columns[] = {"Id","Nome", "Começo", "Fim"};
        String data[][] = new String[screenings.size()][columns.length];
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        int i =0;
        for(Screening s: screenings){
            data[i] = new String[]{
            
            String.valueOf(s.getId()),
                s.getMovie().getName(),
                String.valueOf(s.getStart().format(dtf)),
                String.valueOf(s.getEnd().format(dtf))
            };
            i++;
        
        }
        
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTScreening.setModel(model);
       
    
    }
    
    
    
