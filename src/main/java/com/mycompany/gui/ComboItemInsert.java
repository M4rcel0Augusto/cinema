    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed
        
        try{
            int selectedRow = jTProduct.getSelectedRow();
            if(selectedRow>=0){
                int productId = Integer.parseInt(String.valueOf(jTProduct.getValueAt(selectedRow, 0)));
                
                ProductDAO productDAO = new ProductDAO();
                Product product = productDAO.select(productId);
                
                if(product == null){
                    JOptionPane.showMessageDialog(this, "Item não encontrado");
                    return;
                }
                
                boolean found = false; //Caso não tenha o tipo de item na lista = false
                                       // Caso tenha = true
                
                for(ComboItemTemp item : comboList){
                    if(item.getProduct().getId() ==productId){
                        item.increment();
                        found = true;
                        break;
                    }
                }
                if(!found){
                comboList.add(new ComboItemTemp(product));}
                updateComboTable();
            }
        
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar item no combo");
        }
        
        
    }//GEN-LAST:event_jBAddActionPerformed

    private void jBSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSaveActionPerformed
        if(comboList.isEmpty()){
            JOptionPane.showMessageDialog(this, "Combo vazio.\nAdicione algum item.");
            return;
        }
        try{
            ComboDAO comboDAO = new ComboDAO();
            ComboItemDAO comboItemDAO = new ComboItemDAO();
            
            comboDAO.register(combo);//registrar o id do combo
            
            for (ComboItemTemp i : comboList) {

                ComboItem item = new ComboItem();

                ComboItemId id = new ComboItemId(
                    combo.getId(),
                    i.getProduct().getId()
                );

                item.setId(id);
                item.setCombo(combo);
                item.setProduct(i.getProduct());
                item.setQuantity(i.getQuantity());

                comboItemDAO.register(item);
            }

            JOptionPane.showMessageDialog(this, "Combo Salvo com sucesso");
            parent.loadMaster();
            parent.dispose();
            dispose();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao salvar combo");
            
        
        }
    }//GEN-LAST:event_jBSaveActionPerformed

