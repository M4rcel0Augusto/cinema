    public void loadProductTable(){
        ProductDAO productDAO = new ProductDAO();
        List<Product> products = productDAO.list();
        fillTable(products);
    }
    
    private void fillTable(List<Product> products){
        String columns[]= {"Id", "Nome", "Tipo", "Tamanho", "Preço un", "Custo un"};
        String data [][] = new String[products.size()][columns.length];
        
        int i =0;
        
        for(Product p : products){
            data[i] = new String[]{
                String.valueOf(p.getId()),
                p.getProductName(),
                String.valueOf(p.getProductType()),
                String.valueOf(p.getSize()),
                
                String.valueOf(p.getUnitPrice()),
                String.valueOf(p.getUnitCost())


                
            };
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(data,columns);
        jTProduct.setModel(model);
       
    }
    
    private void updateComboTable(){
        String[] columns= {
        "Produto", "Tipo", "Tamanho","Qt", "Preço un"
        };
        String[][] data = new String[comboList.size()][columns.length];
        
        int i =0;
        for(ComboItemTemp item : comboList){
            data[i] = new String[]{
            item.getProduct().getProductName(),
                String.valueOf(item.getProduct().getProductType()),
                String.valueOf(item.getProduct().getSize()),
                String.valueOf(item.getQuantity()),
                String.valueOf(item.getProduct().getUnitPrice())
            
            
            };i++;
        }
        DefaultTableModel comboModel = new DefaultTableModel(data,columns);
        jTComboItems.setModel(comboModel);
    }
    
